        <p>今天，我們為您帶來一道極具風味的美食體驗：香烤蒜蓉奶油蝦醬意大利麵！這道菜將融合鮮嫩的蝦仁、濃郁的奶油蒜蓉醬汁，搭配彈牙的意大利麵，給您帶來口感和味覺的雙重享受。</p>
        <h2>材料：</h2>
        <ul>
            <li>鮮蝦仁，250克</li>
            <li>意大利麵，200克</li>
            <li>奶油，3湯匙</li>
            <li>蒜瓣，4瓣（切碎）</li>
            <li>鮮檸檬汁，2湯匙</li>
            <li>新鮮香草（如巴西里、迷迭香），適量</li>
            <li>橄欖油，2湯匙</li>
            <li>鹽和胡椒粉，適量</li>
        </ul>
        <h2>步驟：</h2>
        <ol>
            <li>先將蝦仁洗淨，去殼去腸泥，瀝乾備用。同時，在一鍋鹽水中煮意大利麵，煮至彈牙狀，瀝乾備用。</li>
            <li>在平底鍋中，用中小火加熱橄欖油，加入切碎的蒜瓣，翻炒至蒜香四溢。</li>
            <li>加入鮮蝦仁，繼續翻炒，直到蝦仁變色為止。</li>
            <li>加入奶油，讓奶油溶化，與蝦仁充分融合。</li>
            <li>淋入檸檬汁，調味以鹽和胡椒粉，根據個人口味調整。</li>
            <li>將煮熟的意大利麵加入鍋中，與蝦仁、奶油醬汁充分拌勻，確保每根麵條都均勻沾上美味的醬汁。</li>
            <li>最後，灑上新鮮的香草，如巴西里或迷迭香，為菜品增添色彩和風味。</li>
            <li>將完成的香烤蒜蓉奶油蝦醬意大利麵盛裝在碟中，即可享用！</li>
        </ol>
